    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // TODO add your handling code here:

        String correo = txtCorreo.getText();
        String contrase単a = txtContrasenia.getText();
        Usuario respuesta = controladorUsuario.login(correo, contrase単a);
        if (respuesta != null) {
            JOptionPane.showMessageDialog(null, "Bienvenido");
            
            if (respuesta instanceof Cliente) {
                Cliente cliente = (Cliente) respuesta;
                VistaCliente vistaCliente = new VistaCliente(cliente);
                vistaCliente.setVisible(true);
                vistaCliente.setLocationRelativeTo(this);
                this.dispose();
            } else {
                VistaAdministrador vistaAdministrador = new VistaAdministrador();
                vistaAdministrador.setVisible(true);
                vistaAdministrador.setLocationRelativeTo(null);
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Usuario o contrase単a incorrectos");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

