    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int idMaquina = Integer.parseInt(txtIdMaquina.getText());
        int edadMinima = Integer.parseInt(txtEdadMinima.getText());
        String tipoJuego = txtTipoDeJuego.getText();
        String genero = txtGenero.getText();
        double valorHora = Double.valueOf(txtValorHora.getText());
        boolean disponible = jCheckBoxFueraDeServicio.isSelected();
        Maquina maquina = new Maquina(idMaquina, edadMinima, tipoJuego, genero, valorHora, disponible);
        boolean respuesta = controladorMaquina.guardarMaquina(maquina);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "Se guardo");
            llenarTabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "No se guardo");

        }


    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        int idMaquina = Integer.parseInt(txtIdMaquina.getText());
        Maquina respuesta = controladorMaquina.buscarMaquina(idMaquina);
        if (respuesta != null) {
            txtEdadMinima.setText(String.valueOf(respuesta.getEdadMinima()));
            txtTipoDeJuego.setText(respuesta.getTipoDeJuego());
            txtGenero.setText(respuesta.getGenero());
            txtValorHora.setText(String.valueOf(respuesta.getValorHora()));
            JOptionPane.showMessageDialog(null, "Se encontro");

        } else {
            JOptionPane.showMessageDialog(null, "No se encontro");

        }


    }//GEN-LAST:event_btnBuscarActionPerformed

