    private Usuario mostarCliente(Usuario usuario) {
        Usuario aux = controladorUsuario.buscarCliente(usuario.getId());
        if (aux instanceof Cliente) {
            Cliente cliente = (Cliente) aux;
            txtCorreo.setText(cliente.getCorreo());
            txtID.setText(cliente.getId() + "");
            txtNombre.setText(cliente.getNombre());
        }
        return null;
    }

