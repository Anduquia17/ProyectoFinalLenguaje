    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        int idSubscripcion = Integer.parseInt(txtIdSubscripcion.getText());
        TipoSuscripcion tipo = TipoSuscripcion.valueOf(jComboBox1.getSelectedItem().toString());
        int horasDisp = Integer.parseInt(txtHorasDisponibles.getText());
        double valorBase = Double.parseDouble(txtValorBase.getText());
        boolean estadoSubs = jckEstadoSubs.isSelected();
        String nombre = txtNombreCliente1.getText();
        int edad = Integer.parseInt(txtEdadCliente1.getText());
        int id = Integer.parseInt(txtIdCliente1.getText());
        String correo = txtCorreoCliente1.getText();
        String contrase침a = txtContrase침aCliente1.getText();
        String rol = txtRol1.getText();
        Cliente cliente = new Cliente(nombre, edad, id, correo, contrase침a, rol);
        Subscripcion subscripcion = new Subscripcion(idSubscripcion, tipo, horasDisp, valorBase, cliente, LocalDateTime.MIN, LocalDateTime.MIN, estadoSubs);
        boolean respuesta = controladorSuscripcion.guardarSubcripcion(subscripcion, cliente);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "Se guardo la subscripci칩n");
            listarSubs();
        } else {
            JOptionPane.showMessageDialog(null, "No se guardo la subscripci칩n");

        }


    }//GEN-LAST:event_btnGuardarActionPerformed

