    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtIdCliente1.getText());
        Cliente respuesta = controladorUsuario.buscarCliente(id);
        if(respuesta != null){
            txtNombreCliente1.setText(respuesta.getNombre());
            txtEdadCliente1.setText(String.valueOf(respuesta.getEdad()));
            txtIdCliente1.setText(String.valueOf(respuesta.getId()));
            txtCorreoCliente1.setText(respuesta.getCorreo());
            txtContrase침aCliente1.setText(respuesta.getContrase침a());
            txtRol1.setText(respuesta.getRol());
        }else{
            JOptionPane.showMessageDialog(null, "No existe");
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed


